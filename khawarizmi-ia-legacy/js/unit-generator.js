  generateUnitHTML(unitId) {
    const result = PROGRAM.getUnitById(unitId);
    if (!result) return '';
    const { unit, domain } = result;
    const content = this.contentDB[unitId] || [];
    const allUnits = PROGRAM.domains.flatMap(d => d.units.map(u => ({ ...u, domainId: d.id })));
    const idx = allUnits.findIndex(u => u.id === unitId);
    const prev = idx > 0 ? allUnits[idx - 1] : null;
    const next = idx < allUnits.length - 1 ? allUnits[idx + 1] : null;
    const gradient = domain.gradient || 'var(--domain-1-gradient)';

    const chaptersHTML = unit.chapters.map((ch, i) => `
      <section class="chapter-section fade-in" id="ch${i}">
        <div class="chapter-header">
          <div class="chapter-num-box" style="background:${gradient};">${ch.num}</div>
          <div class="chapter-titles">
            <h2 class="chapter-title-ar">${ch.titleAr}</h2>
            <p class="chapter-title-fr">${ch.titleFr}</p>
          </div>
          <span class="chapter-page-ref">📄 ص ${ch.page}</span>
        </div>
        <div class="definition-box">
          <div class="definition-label">المحتوى</div>
          <div class="definition-text">${content[i] || 'المحتوى التفصيلي قيد الإضافة...'}</div>
        </div>
      </section>`).join('\n');

    const sidebarHTML = unit.chapters.map((ch, i) => `
      <a href="#ch${i}" class="sidebar-nav-item${i === 0 ? ' active' : ''}">
        <span class="sidebar-nav-num">${ch.num}</span> ${ch.titleAr}
      </a>`).join('\n');

    const navHTML = `
      <div class="unit-nav-section">
        <div class="unit-nav-inner">
          ${prev ? `<a href="${prev.id}.html" class="unit-nav-card prev">
            <span class="nav-card-arrow">→</span>
            <div class="nav-card-info">
              <div class="nav-card-label">الوحدة السابقة</div>
              <div class="nav-card-title">${prev.icon} ${prev.titleAr}</div>
            </div>
          </a>` : '<div></div>'}
          ${next ? `<a href="${next.id}.html" class="unit-nav-card next">
            <span class="nav-card-arrow">←</span>
            <div class="nav-card-info">
              <div class="nav-card-label">الوحدة التالية</div>
              <div class="nav-card-title">${next.icon} ${next.titleAr}</div>
            </div>
          </a>` : '<div></div>'}
        </div>
      </div>`;

    return { chaptersHTML, sidebarHTML, navHTML };
  },

  generateFullPage(unitId) {
    const result = PROGRAM.getUnitById(unitId);
    if (!result) return '';
    const { unit, domain } = result;
    const gen = this.generateUnitHTML(unitId);
    return {
      title: unit.titleAr + ' | خوارزمي IA',
      unit, domain,
      chaptersHTML: gen.chaptersHTML,
      sidebarHTML: gen.sidebarHTML,
      navHTML: gen.navHTML
    };
  }
