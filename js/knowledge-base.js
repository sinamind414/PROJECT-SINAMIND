  isSVTQuestion(question) {
    const lower = question.toLowerCase();
    let matchCount = 0;
    for (const keyword of this.svtKeywords) {
      if (lower.includes(keyword.toLowerCase())) {
        matchCount++;
        if (matchCount >= 1) return true;
      }
    }
    return false;
  },

  searchKnowledge(question) {
    const lower = question.toLowerCase();
    const results = [];

    for (const [key, entry] of Object.entries(this.knowledge)) {
      let score = 0;
      for (const keyword of entry.keywords) {
        if (lower.includes(keyword.toLowerCase())) score += 10;
      }
      const contentLower = entry.content.toLowerCase();
      const words = lower.split(/\s+/).filter(w => w.length > 3);
      for (const word of words) {
        if (contentLower.includes(word)) score += 2;
      }
      if (score > 0) results.push({ key, entry, score });
    }

    results.sort((a, b) => b.score - a.score);
    return results;
  },

  getBestAnswer(question) {
    const results = this.searchKnowledge(question);
    if (results.length === 0) return null;
    const best = results[0];
    if (best.score < 10) return null;
    return {
      content: best.entry.content,
      key: best.key,
      score: best.score,
      relatedTopics: results.slice(1, 4).map(r => r.key)
    };
  },

